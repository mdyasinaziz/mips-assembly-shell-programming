PRINT MACRO MSG
MOV AH, 09H
LEA DX, MSG
INT 21H
ENDM
 
CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START:
MOV AX, DATA
MOV DS, AX
MOV AL, SE
LEA SI, STRING1
